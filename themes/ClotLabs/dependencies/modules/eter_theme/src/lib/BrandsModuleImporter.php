/**
 * Import class
 */
include_once(_PS_MODULE_DIR_.'eter_brands/classes/eterbrands.php');

class BrandsModuleImporter
{
	/**
     * Main method
     */
	public function run($theme) {
        $jsonData = file_get_contents(_ETER_IMPORT_DIR_.$theme.'/eter_modules/brands/brands.json');
        $data = json_decode($jsonData, true);
		$this->truncateTables();
		$this->installDefaultBrands($data,$theme);
        return ['success' => true];
	}

    /**
     * Install new brands
     */
	public function installDefaultBrands($brands,$theme) {
        $languages = Language::getLanguages(false);
        foreach ($brands as $item) {
            $brand = new EterBrands();
            $brand->image = $item['image'];
            $brand->active = $item['active'];
            $brand->position = $item['position'];
            foreach ($languages as $language) {
                $lang = $language['id_lang'];
                $brand->name[$lang] = $item['name'];
                $brand->url[$lang] = $item['url'];;
            }
            if($brand->add()) {
            	$from = _ETER_IMPORT_DIR_.$theme.'/eter_modules/brands/'.$item['image'];
            	$to = _PS_MODULE_DIR_.'eter_brands/images/'.$item['image'];
            	@copy($from,$to);
            }
        }
    }
    /**
     * Clear Database
     */
    public function truncateTables() {
        $res = Db::getInstance()->execute('TRUNCATE TABLE `'._DB_PREFIX_.'eter_brands`;');
        $res = Db::getInstance()->execute('TRUNCATE TABLE `'._DB_PREFIX_.'eter_brands_shop`;');
        $res = Db::getInstance()->execute('TRUNCATE TABLE `'._DB_PREFIX_.'eter_brands_lang`;');
    }
}