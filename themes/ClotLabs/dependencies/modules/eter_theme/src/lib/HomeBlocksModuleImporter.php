/**
 * Import class
 */
include_once(_PS_MODULE_DIR_.'eter_homeblocks/classes/Block.php');
class HomeBlocksModuleImporter
{
    /**
     * Main method
     */
	public function run($theme) {
        $jsonData = file_get_contents(_ETER_IMPORT_DIR_.$theme.'/eter_modules/homeblocks/homeblocks.json');
        $data = json_decode($jsonData, true);
		$this->truncateTables();
		$this->install($data,$theme);
        return ['success' => true];
	}

    /**
     * Install slides for homeblocks
     */
	public function install($blocks,$theme) {
        $languages = Language::getLanguages(false);
        foreach ($blocks as $item) {
            $slide = new Block();
            $slide->image = $item['image'];
            $slide->active = $item['active'];
            $slide->position = $item['position'];
            foreach ($languages as $language) {
                $lang = $language['id_lang'];
                $slide->title[$lang] = $item['title'];
                $slide->details[$lang] = $item['details'];
                $slide->url_name[$lang] = $item['url_name'];;
                $slide->url[$lang] = $item['url'];;
            }
            if($slide->add()) {
            	$from = _ETER_IMPORT_DIR_.$theme.'/eter_modules/homeblocks/'.$item['image'];
            	$to = _PS_MODULE_DIR_.'eter_homeblocks/images/'.$item['image'];
            	@copy($from,$to);
            }
        }
    }

    /**
     * Clear Database
     */
    public function truncateTables() {
        $res = Db::getInstance()->execute('TRUNCATE TABLE `'._DB_PREFIX_.'eter_blocks_lang`;');
        $res = Db::getInstance()->execute('TRUNCATE TABLE `'._DB_PREFIX_.'eter_blocks_shop`;');
        $res = Db::getInstance()->execute('TRUNCATE TABLE `'._DB_PREFIX_.'eter_blocks`;');
    }
}