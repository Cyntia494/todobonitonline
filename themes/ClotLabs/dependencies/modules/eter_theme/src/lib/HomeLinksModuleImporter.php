class HomeLinksModuleImporter
{
	/**
	*Main method
	**/
	public function run ($theme){
		$jsonData = file_get_contents(_ETER_IMPORT_DIR_.$theme.'/eter_modules/homelinks/homelinks.json');
		$data = json_decode($jsonData, true);
		$this->installHomeLinks($data,$theme);
		return ['success' => true];
	}
	/**
	*Install home links images
	**/
	public function installHomeLinks($sections,$theme){
		$languages = Language::getLanguages(false);
		foreach ($sections as $key =>$section) {
			if(isset($section['image'])) {
				$image = $section['image'];
				$number = str_replace('section','',$key);
				$from = _ETER_IMPORT_DIR_.$theme.'/eter_modules/homelinks/'.$image;
				$to = _PS_MODULE_DIR_.'eter_homelinks/images/'.$image;
				if(@copy($from,$to)){
					Configuration::updateValue('P'.$number.'_IMAGE', $image);
				}
			}
			foreach ($languages as $language) {
				$lang = $language['id_lang'];
				Configuration::updateValue('P'.$number.'_URL_'.$lang, $section['url']);
				Configuration::updateValue('P'.$number.'_TITLE_'.$lang, $section['title']);
				Configuration::updateValue('P'.$number.'_DESC_'.$lang, $section['description']);
			}
		}
	}
}